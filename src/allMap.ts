let cameraX = 0;
let cameraY = 0;
let step = 16;
let ctx, img0, img1, img2, images;

function renderCanvasTotal() {
    //console.log('renderCanvasTotal...');
    ctx.strokeStyle = "blue";
    for (let i = 0; i < map.height; i++) {
        for (let j = 0; j < map.width; j++) {
            ctx.drawImage(
                images[map.data[i][j]],
                j * blockWitdh,
                i * blockHeight);
            //ctx.strokeRect(j * blockWitdh, i * blockHeight, blockWitdh,blockHeight);
        }
    }
}

//
// On Key Down
//
window.onkeydown = function (event) {
    //console.log('window.onkeydown[' + event.key + ']...');
    if (event.key === "ArrowRight") {
        //move origin(0,0) to the left
        ctx.translate(-step,0);
        renderCanvasTotal();
    }
    if (event.key === "ArrowLeft") {
        ctx.translate(step,0);
        renderCanvasTotal();
    }
}

//
// On Window Load
//
window.onload = () => {
    console.log('Window.onLoad...');
    ctx = document.getElementById("canvas").getContext("2d");
    img0 = document.getElementById("block_0");
    img1 = document.getElementById("block_1");
    img2 = document.getElementById("block_2");
    images = [img0, img1, img2];
    renderCanvasTotal();
}